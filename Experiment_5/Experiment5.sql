-- Task-1: Odd Salaries
SELECT EMP_ID, EMP_NAME, SALARY
FROM EMPLOYEE
WHERE MOD(SALARY, 2) = 1

-- Task-2: Even Salaries
SELECT EMP_ID, EMP_NAME, SALARY
FROM EMPLOYEE
WHERE MOD(SALARY, 2) = 0

-- Task-3: Combined Classification
SELECT EMP_ID, EMP_NAME, SALARY,
       CASE 
         WHEN MOD(SALARY, 2) = 0 THEN 'EVEN'
         ELSE 'ODD'
       END AS SALARY_TYPE
FROM EMPLOYEE
